#En esta Funcion, sacamos el Error punto por punto
ErrorPuntoPorPunto = function(h)
{
  Counter = 1
  Position = 1
  errorPto = vector("numeric", length(quixadaTempError))
  
  repeat
  {
    if((Counter == quixadaHorasError[Position]))
    {
      errorPto[Position] = abs(h(Counter) - quixadaTempError[Position])
      Position = Position + 1
    }
    if(Counter == length(quixadaHorasCompleto) || Position == length(quixadaTempError)) break
    Counter = Counter + 1
    
  }
  return (errorPto)
}
#En esta funcion, se obtiene el error cuadratico medio
ErrorCuadraticoMedio = function (h)
{
  Contador = 1
  Posicion = 1
  ValorTot = 0
  repeat
  {
    if(Contador == quixadaHorasError[Posicion])
    {
      #QuixadaTempError es mi valor Observado y h es mi valor Estimado
      ValorTot = ValorTot + (quixadaTempError[Posicion] - h(Contador))^2
      Posicion = Posicion + 1
    }
    if(Contador == length(quixadaHorasCompleto) || Posicion == length(quixadaTempError)) break
    Contador = Contador + 1
  }
  ValorTot = ValorTot / length(quixadaTempError)
  return (ValorTot)
}


ErrorPromedio = function(elVector)
{
  Contador = 1
  Promedio = 0
  repeat
  {
    Promedio = elVector[Contador] + Promedio
    if(Contador == length(elVector)) break
    Contador = Contador + 1
  }
  Promedio = Promedio / length(elVector)
  return(Promedio)
}

